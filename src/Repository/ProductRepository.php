    public function findFeatured()
    {
        $qb = $this->createQueryBuilder('p')->select('p.id', 'p.name', 'p.price', 'p.currency')->leftJoin(
            'p.category', 'c'
        )->addSelect(
            'c.name AS categoryName'
        )->where('p.featured = true');


        $query = $qb->getQuery();

        return $query->execute();
